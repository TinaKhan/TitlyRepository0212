package collectionFramework;

public class UseOfArray0101 {

	public static void main(String[] args) {
		int [] a=new int[7];
		a[0]=10;
		a[1]=20;
		a[2]=30;
		a[5]=50;
		//"Printing single index value "
		System.out.println("Printing single index value "+a[0]);
		System.out.println("Printing single index value "+a[1]);
		System.out.println("Printing single index value "+a[2]);
		System.out.println("Printing single index value "+a[3]);
		System.out.println("Printing single index value "+a[5]);
		System.out.println("Printing single index value "+a.length);
		System.out.println("Printing single index value "+(a.length-1));
		

	}

}
